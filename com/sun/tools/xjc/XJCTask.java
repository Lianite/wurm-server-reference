    private class AngProgressCodeWriter implements CodeWriter
    {
        private final CodeWriter output;
        
        public AngProgressCodeWriter(final CodeWriter output) {
            this.output = output;
        }
        
        public OutputStream open(final JPackage pkg, final String fileName) throws IOException {
            if (pkg.isUnnamed()) {
                XJCTask.this.log("generating " + fileName, 3);
            }
            else {
                XJCTask.this.log("generating " + pkg.name().replace('.', File.separatorChar) + File.separatorChar + fileName, 3);
            }
            return this.output.open(pkg, fileName);
        }
        
        public void close() throws IOException {
            this.output.close();
        }
    }
    
    private class ErrorReceiverImpl extends ErrorReceiver
    {
        public void warning(final SAXParseException e) {
            this.print(1, "Driver.WarningMessage", e);
        }
        
        public void error(final SAXParseException e) {
            this.print(0, "Driver.ErrorMessage", e);
        }
        
        public void fatalError(final SAXParseException e) {
            this.print(0, "Driver.ErrorMessage", e);
        }
        
        public void info(final SAXParseException e) {
            this.print(3, "Driver.InfoMessage", e);
        }
        
        private void print(final int logLevel, final String header, final SAXParseException e) {
            XJCTask.this.log(Messages.format(header, (Object)e.getMessage()), logLevel);
            XJCTask.this.log(this.getLocationString(e), logLevel);
            XJCTask.this.log("", logLevel);
        }
    }
